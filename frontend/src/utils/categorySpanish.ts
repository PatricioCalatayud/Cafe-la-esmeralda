
const categorySpanish = (name: string) => {
    switch (name) {
      case 'Chocolate':
        return "Chocolate";
      case 'Sweetener':
        return "Endulzante";
      case 'Mate':
        return "Mate";
      case 'Accesory':
        return "Accesorios";
      case 'Tea':
        return "Te";
      case 'Cafe':
        return "Cafe";
    }
  };

  export default categorySpanish